def sum(a=0, b=0):
    """Sum between two inputted numbers"""
    s = a + b
    return s


def subtraction(a=0, b=0):
    """Subtraction between two inputted numbers"""
    if a >= b:
        s = a - b
        return s
    else:
        s = b - a 
        return s


def multiply (a=1, b=1):
    """This function multiply the two inputted numbers"""
    m = a * b
    return m

